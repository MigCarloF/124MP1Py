class Student:
    def __init__(self, name, age, gpa, gradeLevel):
        self.name = name
        self.age = age
        self.gpa = gpa
        self.gradeLevel = gradeLevel

    def getName(self):
        return self.name

    def getAge(self):
        return self.age

    def getGpa(self):
        return self.gpa

    def getGradeLevel(self):
        return self.gradeLevel

